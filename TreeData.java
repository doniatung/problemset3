
public class TreeData  {
	private char key;
	private int value;
	
	public TreeData(char k, int v) {
		key = k;
		value = v;
	}

	public char getKey() {
		return key;
	}

	public void setKey(char key) {
		this.key = key;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}
	
}
